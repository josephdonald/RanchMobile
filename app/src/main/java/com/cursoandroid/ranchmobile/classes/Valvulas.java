package com.cursoandroid.ranchmobile.classes;

public class Valvulas {

    private int idValvulas;
    private String nomeValvula;
    private String descricaoValvula;
    private String latitudeValvula;
    private String longitudeValvula;
    private String statusValvula;

    public Valvulas() {
    }

    public int getIdValvulas() {
        return idValvulas;
    }

    public void setIdValvulas(int idValvulas) {
        this.idValvulas = idValvulas;
    }

    public String getNomeValvula() {
        return nomeValvula;
    }

    public void setNomeValvula(String nomeValvula) {
        this.nomeValvula = nomeValvula;
    }

    public String getDescricaoValvula() {
        return descricaoValvula;
    }

    public void setDescricaoValvula(String descricaoValvula) {
        this.descricaoValvula = descricaoValvula;
    }

    public String getLatitudeValvula() {
        return latitudeValvula;
    }

    public void setLatitudeValvula(String latitudeValvula) {
        this.latitudeValvula = latitudeValvula;
    }

    public String getLongitudeValvula() {
        return longitudeValvula;
    }

    public void setLongitudeValvula(String longitudeValvula) {
        this.longitudeValvula = longitudeValvula;
    }

    public String getStatusValvula() {
        return statusValvula;
    }

    public void setStatusValvula(String statusValvula) {
        this.statusValvula = statusValvula;
    }
}