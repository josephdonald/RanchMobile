package com.cursoandroid.ranchmobile.classes;

public class Estacao {

    private int idEstacao;
    private String nomeEstacao;
    private String descricaoEstacao;
    private String latitudeEstacao;
    private String longitudeEstacao;
    private String statusEstacao;
    private int idValvula;

    public Estacao() {

    }

    public int getIdEstacao() {
        return idEstacao;
    }

    public void setIdEstacao(int idEstacao) {
        this.idEstacao = idEstacao;
    }

    public String getNomeEstacao() {
        return nomeEstacao;
    }

    public void setNomeEstacao(String nomeEstacao) {
        this.nomeEstacao = nomeEstacao;
    }

    public String getDescricaoEstacao() {
        return descricaoEstacao;
    }

    public void setDescricaoEstacao(String descricaoEstacao) {
        this.descricaoEstacao = descricaoEstacao;
    }

    public String getLatitudeEstacao() {
        return latitudeEstacao;
    }

    public void setLatitudeEstacao(String latitudeEstacao) {
        this.latitudeEstacao = latitudeEstacao;
    }

    public String getLongitudeEstacao() {
        return longitudeEstacao;
    }

    public void setLongitudeEstacao(String longitudeEstacao) {
        this.longitudeEstacao = longitudeEstacao;
    }

    public String getStatusEstacao() {
        return statusEstacao;
    }

    public void setStatusEstacao(String statusEstacao) {
        this.statusEstacao = statusEstacao;
    }

    public int getIdValvula() {
        return idValvula;
    }

    public void setIdValvula(int idValvula) {
        this.idValvula = idValvula;
    }
}
